numero = int(input("escribe un numero del 1 al 6 "))
lista = ["p", "y", "h", "t", "o", "n"]
if numero == 1:
    print(lista[0])
elif numero == 2:
    print(lista[1])
elif numero == 3:
    print(lista[2])
elif numero == 4:
    print(lista[3])
elif numero == 5:
    print(lista[4])
elif numero == 6:
    print(lista[5])
else:
    print("el numero no es valido")